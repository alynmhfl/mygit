percent_float = float(input('what is your percentage?'))

if 90<=percent_float <100:
    print ("you get an A")
elif 80<=percent_float<90:
    print('you get a B')
elif 70<=percent_float>80:
    print ('you get a C')
elif 60<=percent_float>70:
    print ('you get a D')
else:
    print ('oops, no good')